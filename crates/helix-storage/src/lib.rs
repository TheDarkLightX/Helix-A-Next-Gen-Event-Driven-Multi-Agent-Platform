//! Provides abstractions for interacting with storage backends (Postgres, Redpanda, SurrealDB, Qdrant).

pub fn add(left: usize, right: usize) -> usize {
    left + right
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn it_works() {
        let result = add(2, 2);
        assert_eq!(result, 4);
    }
}
